#include "libft.h"

int	ft_isdigit(char *av)
{
	int	i;

	i = 0;
	if (av[i] == '\0')
		return (0);
	if ((av[i] == '+' || av[i] == '-') && av[i + 1] != '\0')
		i++;
	while (av[i])
	{
		if (av[i] >= '0' && av[i] <= '9')
			i++;
		else
			return (0);
	}
	return (1);
}
